namespace NotPaint
{
    static class Extensions
    {
        public static double Distance(this Point a, Point b)
        {
            return Math.Sqrt((a.X - b.X) * (a.X - b.X) + (a.Y - b.Y) * (a.Y - b.Y));
        }
    }
